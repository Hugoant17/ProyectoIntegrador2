package com.example.proyectointegrador2.Modelo;

public class Producto {

    private String codigo, nombre, codTienda, descripcion, categoria, foto;
    private int stock;
    private double precio;

    public Producto() {

    }

    public Producto(String codigo, String nombre, String codTienda, String descripcion, String categoria, String foto, int stock, double precio) {
        this.codigo = codigo;
        this.nombre = nombre;
        this.codTienda = codTienda;
        this.descripcion = descripcion;
        this.categoria = categoria;
        this.foto = foto;
        this.stock = stock;
        this.precio = precio;
    }

    public String getCodigo() {
        return codigo;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getCodTienda() {
        return codTienda;
    }

    public void setCodTienda(String codTienda) {
        this.codTienda = codTienda;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public String getCategoria() {
        return categoria;
    }

    public void setCategoria(String categoria) {
        this.categoria = categoria;
    }

    public String getFoto() {
        return foto;
    }

    public void setFoto(String foto) {
        this.foto = foto;
    }

    public int getStock() {
        return stock;
    }

    public void setStock(int stock) {
        this.stock = stock;
    }

    public double getPrecio() {
        return precio;
    }

    public void setPrecio(double precio) {
        this.precio = precio;
    }
}
